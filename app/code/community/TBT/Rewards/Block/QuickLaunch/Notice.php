/**
 * Quick Launch Notice Block
 *
 * @category   TBT
 * @package    TBT_Rewards
 * @author     Sweet Tooth Inc. <support@sweettoothrewards.com>
 */
class TBT_Rewards_Block_QuickLaunch_Notice extends Mage_Core_Block_Template 
{
    protected function _construct() 
    {
        $this->setTemplate('rewards/quickLaunch/notice.phtml');
    }
    
    public function getLastLaunchDate()
    {
        $formatedDate = false;
        $utcDatetime = Mage::getStoreConfig('rewards/general/last_quick_launch');

        if ($utcDatetime) {
            $zendDate = Mage::helper('rewards/datetime')->getZendDate($utcDatetime, true);
            $formatedDate = $zendDate->get(Zend_Date::DATE_LONG);
        }
        
        return $formatedDate;
    }
}
